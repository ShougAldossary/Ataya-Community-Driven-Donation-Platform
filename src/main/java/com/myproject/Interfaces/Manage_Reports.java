    private void add_notesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_notesActionPerformed
            int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a report!");
        return;
    }

    int donationId = (int) jTable1.getValueAt(row, 0);

    JTextArea textArea = new JTextArea(5, 30);
    textArea.setLineWrap(true);
    textArea.setWrapStyleWord(true);

    int result = JOptionPane.showConfirmDialog(
            this,
            new JScrollPane(textArea),
            "Add / Update Notes",
            JOptionPane.OK_CANCEL_OPTION
    );

    if (result != JOptionPane.OK_OPTION)
        return;

    String notes = textArea.getText().trim();

    try (Connection conn = DBConnection.getConnection()) {
        String sql = "UPDATE Donation_Reports SET notes = ? WHERE donation_id = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, notes);
        ps.setInt(2, donationId);
        ps.executeUpdate();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error updating notes!");
    }

    loadReportsTable();
        
    }//GEN-LAST:event_add_notesActionPerformed

