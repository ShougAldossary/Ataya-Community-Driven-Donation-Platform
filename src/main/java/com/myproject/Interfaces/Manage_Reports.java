public void loadReportsTable() {

        String[] columns = {"donation_id", "donor_id", "donee_id", "item_id", "state", "notes"};
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(columns);

        try (Connection conn = DBConnection.getConnection()) {

            String query =
                "SELECT donation_id, donor_id, donee_id, item_id, state, notes " +
                "FROM Donation_Reports";

            PreparedStatement ps = conn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {

                int donationId = rs.getInt("donation_id");
                int donorId = rs.getInt("donor_id");
                int doneeId = rs.getInt("donee_id");
                int itemId = rs.getInt("item_id");
                String state = rs.getString("state");
                String notes = rs.getString("notes");

                model.addRow(new Object[]{donationId, donorId, doneeId, itemId, state, notes});
            }

            jTable1.setModel(model);

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

