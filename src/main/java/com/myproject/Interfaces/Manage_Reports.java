    private void update_statusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_statusActionPerformed

int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a report!");
        return;
    }

    int donationId = (int) jTable1.getValueAt(row, 0);
    int donorId = (int) jTable1.getValueAt(row, 1);
    int doneeId = (int) jTable1.getValueAt(row, 2);
    int itemId = (int) jTable1.getValueAt(row, 3);
    String currentState = (String) jTable1.getValueAt(row, 5);

    String[] states = {"in_progress", "completed", "cancelled"};

    JComboBox<String> combo = new JComboBox<>(states);
    combo.setSelectedItem(currentState);

    int result = JOptionPane.showConfirmDialog(
            this,
            combo,
            "Select new state",
            JOptionPane.OK_CANCEL_OPTION
    );

    if (result != JOptionPane.OK_OPTION) {
        return;
    }

    String newState = (String) combo.getSelectedItem();

    try (Connection conn = DBConnection.getConnection()) {

        conn.setAutoCommit(false);
        
        String updateReportSql =
                "UPDATE Donation_Reports SET state = ? WHERE donation_id = ?";
        try (PreparedStatement ps = conn.prepareStatement(updateReportSql)) {
            ps.setString(1, newState);
            ps.setInt(2, donationId);
            ps.executeUpdate();
        }

        //If donation completed → update item state
        if ("completed".equals(newState)) {
            String updateItemSql =
                    "UPDATE Items SET status = 'completed' WHERE item_id = ?";
            try (PreparedStatement ps = conn.prepareStatement(updateItemSql)) {
                ps.setInt(1, itemId);
                ps.executeUpdate();
             NotificationManage.sendDonationCompletedNotification(conn, donorId, itemId);
             NotificationManage.sendDonationCompletedNotification(conn, doneeId, itemId);
            }
        }

        conn.commit();

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error updating status!");
    }

    loadReportsTable();
    }//GEN-LAST:event_update_statusActionPerformed

    private void delete_reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_reportActionPerformed

    int row = jTable1.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a report!");
        return;
    }

    int donationId = (int) jTable1.getValueAt(row, 0);
    String state = (String) jTable1.getValueAt(row, 5);

    // only delete cancelled donations
    if (!"cancelled".equalsIgnoreCase(state)) {
        JOptionPane.showMessageDialog(this,
                "You can delete a report only if the donation is cancelled.",
                "Delete Not Allowed",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "Delete this cancelled donation report?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm != JOptionPane.YES_OPTION)
        return;

    try (Connection conn = DBConnection.getConnection()) {
        String sql = "DELETE FROM Donation_Reports WHERE donation_id = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, donationId);
        ps.executeUpdate();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error deleting report!");
    }

    loadReportsTable();
 
    }//GEN-LAST:event_delete_reportActionPerformed

