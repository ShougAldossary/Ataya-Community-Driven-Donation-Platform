    private void Admin3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin3ActionPerformed
        // TODO add your handling code here
        int selectedRow = jTable2.getSelectedRow();
    
    if (selectedRow != -1) {
        int userId = (int) jTable2.getValueAt(selectedRow, 0);

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this, 
            "Are you sure you want to delete this customer?", 
            "Confirm Delete", 
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try (Connection conn = DBConnection.getConnection()) {
                String sql = "UPDATE Users SET is_active = FALSE WHERE user_id = ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, userId);
                ps.executeUpdate();

                
                loadCustomersTable();

                javax.swing.JOptionPane.showMessageDialog(this, "Customer deactivated successfully!");
            } catch (SQLException e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error deleting customer: " + e.getMessage());
            }
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a customer first!");
    }
    }//GEN-LAST:event_Admin3ActionPerformed

