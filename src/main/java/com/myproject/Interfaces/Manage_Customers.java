private void loadCustomersTable() {
    String[] columns = {"user_id", "username", "email", "password", "phone", "location"};
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(columns);

    try (Connection conn = DBConnection.getConnection()) {
        String query = "SELECT user_id, username, email, password, phone, "
                + "location FROM Users WHERE user_type = 'customer'" +
                "AND is_active = TRUE";
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            int userId = rs.getInt("user_id");
            String username = rs.getString("username");
            String email = rs.getString("email");
            String password = rs.getString("password");
            String phone = rs.getString("phone");
            String location = rs.getString("location");

            model.addRow(new Object[]{userId, username, email, password, phone,location});
        }

        jTable2.setModel(model);

    } catch (SQLException e) {
        e.printStackTrace();
        System.out.println("Error loading customers: " + e.getMessage());
    }
}



