    private void Update_PasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_PasswordActionPerformed
       String newPassword = Full_Password.getText();
        String confirmPassword = Full_ConfirmPassword.getText();

        if (!newPassword.equals(confirmPassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match.");
            return;
        }

        if (!ValidationUtils.isValidPassword(newPassword)) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 characters, with uppercase, lowercase, number, and special character.");
            return;
        }

        try (Connection con = DBConnection.getConnection()) {
            String sql = "UPDATE Users SET password = ? WHERE user_id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, newPassword);
            pst.setInt(2, userId);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Password updated successfully!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating password: " + e.getMessage());
        }
    }//GEN-LAST:event_Update_PasswordActionPerformed

