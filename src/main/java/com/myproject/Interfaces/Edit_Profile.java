            private void loadUserData() {
        try (Connection con = DBConnection.getConnection()) {
            String sql = "SELECT * FROM Users WHERE user_id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, userId);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                  // Fill the form fields with user data
                Full_Email.setText(rs.getString("email"));
                Full_Username.setText(rs.getString("username"));
                Full_Password.setText(rs.getString("password")); 
                Full_ConfirmPassword.setText(rs.getString("password"));
                Full_Phone.setText(rs.getString("phone"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading user data: " + e.getMessage());
        }
    }
    

