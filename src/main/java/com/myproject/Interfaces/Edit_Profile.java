    private void Update_EmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_EmailActionPerformed
        String newEmail = Full_Email.getText().trim();
        if (!ValidationUtils.isValidEmail(newEmail)) {
            JOptionPane.showMessageDialog(this, "Please enter a valid email (example@example.com).");
            return;
        }
        try (Connection con = DBConnection.getConnection()) {
            String sql = "UPDATE Users SET email = ? WHERE user_id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, newEmail);
            pst.setInt(2, userId);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Email updated successfully!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error updating email: " + e.getMessage());
        }
    }//GEN-LAST:event_Update_EmailActionPerformed

