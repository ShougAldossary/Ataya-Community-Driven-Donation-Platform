    private void Admin5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin5ActionPerformed
        // TODO add your handling code here
        int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        int itemId = (int) jTable1.getValueAt(selectedRow, 0);
        String state = (String) jTable1.getValueAt(selectedRow, 6);
            
    // only delete rejected items
    if (!"rejected".equalsIgnoreCase(state)) {
        JOptionPane.showMessageDialog(this,
                "You can delete an item only if the itemm is rejected.",
                "Delete Not Allowed",
                JOptionPane.WARNING_MESSAGE);
        return;
    }
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to delete this item?", 
                "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try (Connection conn = DBConnection.getConnection()) {
                String sql = "DELETE FROM Items WHERE item_id= ?";
                PreparedStatement ps = conn.prepareStatement(sql);
                ps.setInt(1, itemId);
                ps.executeUpdate();
            } catch (SQLException e) {
                e.printStackTrace();
            }
            loadItemsTable();
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select an item first!");
    }
    }//GEN-LAST:event_Admin5ActionPerformed

