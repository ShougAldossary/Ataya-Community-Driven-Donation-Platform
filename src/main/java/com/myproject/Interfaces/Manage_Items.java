    public void loadItemsTable() {
    String[] columns = {"item_id", "user_id", "title", "description", "item_type", "item_condition", "status", "image_url"};
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(columns);

    try (Connection conn = DBConnection.getConnection()) {
        String query = "SELECT item_id, user_id, title, description, item_type, item_condition, status, image_url FROM Items";
        PreparedStatement ps = conn.prepareStatement(query);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            int itemId = rs.getInt("item_id");
            int userId = rs.getInt("user_id");
            String title = rs.getString("title");
            String description = rs.getString("description");
            String itemType = rs.getString("item_type");
            String itemCondition = rs.getString("item_condition");
            String status = rs.getString("status");
            String imageUrl = rs.getString("image_url");

            model.addRow(new Object[]{itemId, userId, title, description, itemType, itemCondition, status, imageUrl});
        }

        jTable1.setModel(model);

    } catch (SQLException e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this, "Error loading items from database!");
    }
}


