    private void Admin3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Admin3ActionPerformed
        // TODO add your handling code here
         int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        int itemId = (int) jTable1.getValueAt(selectedRow, 0);

        try (Connection conn = DBConnection.getConnection()) {
            String sql = "UPDATE Items SET status = 'approved', approved_at = CURRENT_TIMESTAMP,"
                    + " approved_by = ? WHERE item_id = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, userId);
            ps.setInt(2, itemId);
            ps.executeUpdate();
            
            int ownerId = -1;
            String ownerSql = "SELECT user_id FROM Items WHERE item_id = ?";
            PreparedStatement ownerPs = conn.prepareStatement(ownerSql);
            ownerPs.setInt(1, itemId);
            ResultSet rs = ownerPs.executeQuery();

        if (rs.next()) {
            ownerId = rs.getInt("user_id");
            NotificationManage.sendItemApprovedNotification(conn,ownerId,itemId);
        }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        loadItemsTable();
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select an item first!");
    }
    }//GEN-LAST:event_Admin3ActionPerformed

