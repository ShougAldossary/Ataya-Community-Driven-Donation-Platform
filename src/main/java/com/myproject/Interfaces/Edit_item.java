    private void Edit_itemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Edit_itemActionPerformed
   try {
        String title = Full_Title.getText().trim();
        String description = Full_Details.getText().trim();
        Item.ItemType type = Item.ItemType.valueOf(TypeComboBox.getSelectedItem().toString().toUpperCase());
        Item.ItemCondition condition = Item.ItemCondition.valueOf(ConditionComboBox.getSelectedItem().toString().toUpperCase());
    


        String imageUrl = currentImageUrl;
   if (selectedImageFile != null) {
    String resourcePath = "src/main/resources/itemsPhotos";
    File destFolder = new File(resourcePath);
    if (!destFolder.exists()) destFolder.mkdirs();

    String newFileName = System.currentTimeMillis() + "_" + selectedImageFile.getName();
    File destFile = new File(destFolder, newFileName);
    Files.copy(selectedImageFile.toPath(), destFile.toPath(), StandardCopyOption.REPLACE_EXISTING);

    imageUrl = "itemsPhotos/" + newFileName;
}

        Item newItem = new Item(userId, title, description, type, condition, imageUrl);
        newItem.setItemId(itemId);
        if (newItem.isValid()) {

            boolean success = Item.updateItem(newItem);

            if (success) {
                JOptionPane.showMessageDialog(this, "Item updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add item. Try again.");
            }
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                "Error: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);

    }
    }//GEN-LAST:event_Edit_itemActionPerformed

