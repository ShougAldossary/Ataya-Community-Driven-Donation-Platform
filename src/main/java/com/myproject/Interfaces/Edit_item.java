    private void Upload_PhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Upload_PhotoActionPerformed
    JFileChooser fileChooser = new JFileChooser();

    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
            "Image Files", "jpg", "jpeg", "png", "gif"));

    int result = fileChooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {

        File selectedFile = fileChooser.getSelectedFile();
        String fileName = selectedFile.getName().toLowerCase();

        if (!(fileName.endsWith(".png") || fileName.endsWith(".jpg") ||
              fileName.endsWith(".jpeg") || fileName.endsWith(".gif"))) {

            JOptionPane.showMessageDialog(this, "Please select a valid image file!");
            return;
        }

        selectedImageFile = selectedFile;

        ImageIcon originalIcon = new ImageIcon(selectedFile.getAbsolutePath());
        Image img = originalIcon.getImage();

        int w = Upload_Photo.getWidth();
        int h = Upload_Photo.getHeight();
        int imgW = img.getWidth(null);
        int imgH = img.getHeight(null);

        double scale = Math.min((double) w / imgW, (double) h / imgH);

        int newW = (int) (imgW * scale);
        int newH = (int) (imgH * scale);

        Image scaled = img.getScaledInstance(newW, newH, Image.SCALE_SMOOTH);
        Upload_Photo.setIcon(new ImageIcon(scaled));
        photoUrl.setText(selectedFile.getName());
    }


        
    }//GEN-LAST:event_Upload_PhotoActionPerformed

