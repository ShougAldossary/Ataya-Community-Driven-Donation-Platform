private void loadIteminfo() {

    try {
        Item item = Item.getItemById(itemId);

        if (item == null) {
            JOptionPane.showMessageDialog(this, "Error: Item not found.");
            return;
        }

        // Text fields
        Full_Title.setText(item.getTitle());
        Full_Details.setText(item.getDescription());

        // Image
        currentImageUrl = item.getImageUrl();
        photoUrl.setText(currentImageUrl);
        displayDefaultItemImage(currentImageUrl);

        // ComboBoxes
        TypeComboBox.setSelectedItem(item.getItemType().name());
        ConditionComboBox.setSelectedItem(item.getItemCondition().name());

        // Reset selected image (no new image chosen yet)
        selectedImageFile = null;

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(
            this,
            "Error loading item data: " + e.getMessage(),
            "Error",
            JOptionPane.ERROR_MESSAGE
        );
    }
}


private void displayDefaultItemImage(String imagePathFromDB) {

    if (imagePathFromDB == null || imagePathFromDB.isEmpty()) {
        Upload_Photo.setIcon(null);
        return;
    }

    ImageIcon icon = new ImageIcon("src/main/resources/" + imagePathFromDB);
    Image img = icon.getImage();

    int labelWidth = Upload_Photo.getWidth();
    int labelHeight = Upload_Photo.getHeight();

    int imgWidth = img.getWidth(null);
    int imgHeight = img.getHeight(null);

    if (imgWidth <= 0 || imgHeight <= 0) {
        Upload_Photo.setIcon(null);
        return;
    }

    double scale = Math.min(
        (double) labelWidth / imgWidth,
        (double) labelHeight / imgHeight
    );

    int newW = (int) (imgWidth * scale);
    int newH = (int) (imgHeight * scale);

    Image scaled = img.getScaledInstance(newW, newH, Image.SCALE_SMOOTH);
    Upload_Photo.setIcon(new ImageIcon(scaled));
}






